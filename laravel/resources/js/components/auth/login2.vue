import axios from "axios";
import { mapGetters, mapActions } from "vuex";

export default {
  name: "SignIn",

  data() {
    return {
      form: {
        email: "",
        password: "",
      },
      errors: {},
    };
  },
  computed: {
    ...mapGetters({
      authenticated: "auth/authenticated",
      user: "auth/user",
    }),
  },
  methods: {
    ...mapActions({
      signIn: "auth/signIn",
    }),

    async submit() {
      var that = this;
      await this.signIn(this.form, this);

      this.$router.replace({ name: "feed", params: { slug: this.user.slug } });
    },

    checkForm: function(e) {
      this.errors = {};

      if (!this.form.password) {
        //this.errors.push("Name required.");
        this.errors["password"] = "Password required";
      }

      if (!this.form.email) {
        //this.errors.push('Email required.');
        this.errors["email"] = "Email required";
      } else if (!this.validEmail(this.form.email)) {
        this.errors["email_validation"] = "not valid email";
      }

      if (
        Object.keys(this.errors).length === 0 &&
        this.errors.constructor === Object
      ) {
        console.log(this.$parent.$options.components);
        this.submit();
      }
    },
    validEmail: function(email) {
      var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
      return re.test(email);
    },
    container: document.getElementById("container"),
    signUpButton: () => {
      container.classList.add("right-panel-active");
    },

    signInButton: () => {
      container.classList.remove("right-panel-active");
    },
  },
  mounted() {
    this.$store.commit("set", {
      property_name: "current_component",
      property_values: "",
    });
    console.log(process.env.MIX_APP_URL);
  },
};
